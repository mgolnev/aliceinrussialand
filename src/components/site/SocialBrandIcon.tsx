/** Обводка в стиле Instagram / Lucide. */
function SvgFrame({
  size = 20,
  className,
  children,
  fill = "none",
  stroke = "currentColor",
  strokeWidth = 2,
}: {
  size?: number;
  className?: string;
  children: ReactNode;
  fill?: string;
  stroke?: string;
  strokeWidth?: number;
}) {
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      className={className}
      fill={fill}
      stroke={stroke}
      strokeWidth={strokeWidth}
      strokeLinecap="round"
      strokeLinejoin="round"
      aria-hidden
    >
      {children}
    </svg>
  );
}

/** Заливка (логотипы Simple Icons / упрощённые), читаемы в 20px. */
function FilledPath({
  size,
  className,
  d,
}: {
  size?: number;
  className?: string;
  d: string;
}) {
  return (
    <svg
      width={size ?? 20}
      height={size ?? 20}
      viewBox="0 0 24 24"
      className={className}
      fill="currentColor"
      aria-hidden
    >
      <path d={d} />
    </svg>
  );
}

